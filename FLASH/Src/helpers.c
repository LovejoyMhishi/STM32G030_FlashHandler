void TimeOut(uint32_t time_ms) {

	uint32_t TickStart = HAL_GetTick();
	/*
	 * Hold the CPU and wait
	 */
	while (HAL_GetTick() - TickStart < time_ms) {


	}
	/*
	 * Done waiting
	 */
	return;

}























